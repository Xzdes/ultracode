//! Заглушка QR-модуля.
//!
//! План реализации:
//! 1) Поиск finder patterns (три «бычьих глаза») через соотношения 1:1:3:1:1 по run-length на множестве строк/столбцов.
//! 2) Верификация геометрии трёх паттернов, оценка перспективы и размера версии.
//! 3) Проективное преобразование (warp) в модульную матрицу N×N.
//! 4) Чтение формата/маски, декодирование данных, исправление ошибок (Reed–Solomon GF(256)).
//!
//! Всё без зависимостей. Начать можно с версий 1–5, затем расширять.

#[derive(Debug, Clone, Copy)]
pub struct QrOptions {
    /// Сколько строк/столбцов пробегать для поиска паттернов.
    pub scan_lines: usize,
}

impl Default for QrOptions {
    fn default() -> Self {
        Self { scan_lines: 24 }
    }
}
